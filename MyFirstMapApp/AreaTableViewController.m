#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return self.areas.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSString *currentArea = self.areas[indexPath.row];
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"AreaCell" forIndexPath:indexPath];

    // Configure the cell...
    cell.textLabel.text = currentArea;
    
    return cell;
}

- (IBAction)cancel:(UIStoryboardSegue *)segue
{
    
}
- (IBAction)done:(UIStoryboardSegue *)segue
{
    MapViewController *vc = segue.sourceViewController;
    NSString *area = vc.areaName;
    
    [self.areas addObject:area];
    
    [self.tableView reloadData];
    
}

